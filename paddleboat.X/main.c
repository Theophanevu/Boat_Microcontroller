// --- init the PIC18F device
void initMyPIC18F(void)
{
	// PORTA digital
	ADCON1 = 0x0F ;
	ADCON0 = 0;

	// set ports as OUTPUTS
	TRISB = 0x00;
	TRISC = 0x00;
	TRISD = 0x00;
	TRISE = 0x00;
    
    // set PORTA as input
    TRISA = 0xFF;

	// set port by port on "all zeros"
	PORTB = 0x00;
	PORTC = 0x00;
	PORTD = 0x00;

	PORTE = 0x00;

}

